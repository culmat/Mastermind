    public void paint(int x, int y, int ColorInt) {
        Graphics2D g = (Graphics2D) GFrame.getGraphics();
        RenderingHints hints = new RenderingHints(RenderingHints.KEY_ANTIALIASING, RenderingHints.VALUE_ANTIALIAS_ON);
        g.setRenderingHints(hints);
        switch (ColorInt){
            case 0: g.setColor(Color.red);
                    break;
            case 1: g.setColor(Color.green);
                    break;
            case 2: g.setColor(Color.blue);
                    break;
            case 3: g.setColor(Color.orange);
                    break;
            case 4: g.setColor(Color.cyan);
                    break;
            case 5: g.setColor(Color.magenta);
                    break;
        }
            
        g.fillOval(x, y, 50, 50);
    }
    
    private void StartButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_StartButtonMouseClicked
        // TODO add your handling code here:
        Graphics2D g = (Graphics2D) GFrame.getGraphics();
        RenderingHints hints = new RenderingHints(RenderingHints.KEY_ANTIALIASING, RenderingHints.VALUE_ANTIALIAS_ON);
        g.setRenderingHints(hints);
        int s = 1;                                                              //scale
        g.drawRect(s * 20, s * 20, s * 300, s * 720);
        for (int v = 0; v < 4; v++) {
            for (int h = 0; h < 10; h++) {
                g.drawOval(v * 70 + s * 40, h * 70 + s * 40, s * 50, s * 50);
            }
        }
        int cg[][] = new int[4][10];                                            //Player Color Array
        int cs[] = new int[4];                                                  //Solution Color Array
        for (int i1 = 0; i1 < 4; i1++) {
            cs[i1] = 1 + (int) (Math.random() * 6);
        }
        System.out.println(Arrays.toString(cs));
        
        int whitePin[] = new int[10];
        int blackPin[] = new int[10];
        
        for (int t1 = 0; t1 < 10; t1++){
            for (int t2 = 0; t2 < 4; t2++){
                cg[t2][t1] = 1 + (int) (Math.random() * 6);
            }
        }
        
        //System.out.println(Arrays.toString(cg));
        
        
        for (int i2 = 0; i2 < 10; i2++){
            for (int i3 = 0; i3 < 4; i3++){
                for (int i4 = 0; i4 < 4; i4++){
                    if (cg[i4][i2] == cs[i3]){
                        whitePin[i2]++;
                        System.out.println(Arrays.toString(whitePin));
                    }
                }
                if (cg[i3][i2] == cs[i3]){
                    blackPin[i2]++;
                    System.out.println(Arrays.toString(blackPin));
                }
            }
        }
        for (int cd = 0; cd<6; cd++){
            paint((cd+1)*50,(cd+1)*50,cd);
        }
        
    }//GEN-LAST:event_StartButtonMouseClicked

